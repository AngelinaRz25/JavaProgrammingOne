package Chapter1;

/**
 * Program to display the area and perimeter of a circle to the console
 *
 * @author Angelina Zheng
 */
public class C1_8 {

    /**
     * Main Method
     *
     * @param args arguments from command line prompt
     */
    public static void main(String[] args) {
        System.out.println("The perimeter of the circle is " + (2 * 5.5 * 3.14159));
        System.out.println("The area of the circle is " + (5.5 * 5.5 * 3.14159));
    }
}
